// 变量的使用

public class Var02 {
	public static void main(String[] args) {
		//记录人的信息
		int age = 25;
		double weight = 75.5;
		char gender = '男';
		String name = "周广京";
        // 输出信息
		System.out.println("人的基本信息如下：");
		System.out.println(age);
		System.out.println(weight);
		System.out.println(gender);
		System.out.println(name);

		
	}
}